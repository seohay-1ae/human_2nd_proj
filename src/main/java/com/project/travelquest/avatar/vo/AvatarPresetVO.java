package com.project.travelquest.avatar.vo;

public class AvatarPresetVO {
    private String userEmail;
    private String hatsId;
    private String topsId;
    private String bottomsId;
    private String handsId;
    private String skinsId;

    // Getter/Setter
    public String getUserEmail() {
        return userEmail;
    }
    public void setUserEmail(String userEmail) {
        this.userEmail = userEmail;
    }

    public String getHatsId() {
        return hatsId;
    }
    public void setHatsId(String hatsId) {
        this.hatsId = hatsId;
    }

    public String getTopsId() {
        return topsId;
    }
    public void setTopsId(String topsId) {
        this.topsId = topsId;
    }

    public String getBottomsId() {
        return bottomsId;
    }
    public void setBottomsId(String bottomsId) {
        this.bottomsId = bottomsId;
    }

    public String getHandsId() {
        return handsId;
    }
    public void setHandsId(String handsId) {
        this.handsId = handsId;
    }

    public String getSkinsId() {
        return skinsId;
    }
    public void setSkinsId(String skinsId) {
        this.skinsId = skinsId;
    }
}